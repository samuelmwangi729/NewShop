     */
    public function store(Request $request)
    {
        $rule=[
            'Term'=>'required'
        ];
        $this->validate($request,$rule);
        $term=Term::orderBy('id','asc')->get()->first();
        if($term){
            $term->Terms=$request->Term;
            $term->save();
        }else{
            $term=Term::create([
                'Terms'=>$request->Term
            ]);
        }
        Session::flash('success','Terms Successfully Updated');
        return redirect(route('terms.index'));
    }

